CREATE TABLE funcionarios (
    matricula VARCHAR(20) PRIMARY KEY,
    nome VARCHAR(50),
    sobrenome VARCHAR(50),
    sexo VARCHAR(10),
    dataNascimento VARCHAR(10),
    cpf VARCHAR(14),
    rg VARCHAR(12),
    funcao VARCHAR(50),
    email VARCHAR(100),
    telefone VARCHAR(20),
    rua VARCHAR(100),
    numero INT,
    bairro VARCHAR(50),
    cidade VARCHAR(50),
    cep VARCHAR(10),
    estado VARCHAR(50)
);
